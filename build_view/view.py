# not a dataclass!
class Context(ViewItem):
  def __init__(self):
    self._bounds = None
    self._items = list()

  @property
  def items(self) -> list[ViewItem]:
    return self._items

  def add_item(self, item: ViewItem):
    self.items.append(item)
    self._bounds = None
    return item

  @property
  def bounds(self):
    if self._bounds is None:
      bounds = Rect(0,0,0,0)
      for i in self.items:
        # dprint(f"{type(self)}:{bounds} -> ", end='')
        bounds = bounds.enclose(i)
        # dprint(f"{bounds}")
      self._bounds = bounds
    return self._bounds

  @bounds.setter
  def bounds(self, b):
    self._bounds = b

  def end(self):
    pass


@dataclass
class Conditional(Context):
  condition: str
  ifTrue: list[ViewItem] = field(default_factory=list)
  ifFalse: list[ViewItem] = field(default_factory=list)

  def __post_init__(self):
    super().__init__()

  def isTrue(self) -> bool:
    self._items = self.ifTrue
    return True

  def isFalse(self) -> bool:
    self._items = self.ifFalse
    return True

  def end(self):
    self._items = []

  @property
  def bounds(self):
    if self._bounds is None:
      bounds = Rect(0,0,0,0)
      for i in self.ifTrue + self.ifFalse:
        # dprint(f"{type(self)}:{bounds} -> ", end='')
        bounds = bounds.enclose(i)
        # dprint(f"{bounds}")
      self._bounds = bounds
    return self._bounds

  @bounds.setter
  def bounds(self, b):
    self._bounds = b

  def accept(self, visitor):
    visitor.visitConditional(self)


@dataclass
class Group(Context):
  id: str
  _offset: Vector

  def __post_init__(self):
    super().__init__()
    self._extra: list[Rect] = list()
    self._intrinsic_bounds: Rect|None = None

  def add_item(self, item: ViewItem):
    super().add_item(item)
    self._intrinsic_bounds = None
    return item

  def addExtra(self, extra: Rect):
    self._extra.append(extra)
    self._intrinsic_bounds = None

  @property
  def intrinsic_bounds(self):
    if self._intrinsic_bounds is None:
      bounds = Rect(0,0,0,0)
      for e in self._extra:
        bounds = bounds.union(e)

      for i in self.items:
        # dprint(f"{type(self)}:{bounds} -> ", end='')
        bounds = bounds.enclose(i)
        # dprint(f"{bounds}")
      self._intrinsic_bounds = bounds
    return self._intrinsic_bounds

  @property
  def bounds(self):
    if self._bounds is None:
      self._bounds = self.intrinsic_bounds + self._offset
    return self._bounds

  @bounds.setter
  def bounds(self, b):
    self._bounds = b

  @property
  def offset(self):
    return self._offset

  @offset.setter
  def offset(self, o: Vector):
    self._offset = o
    self._bounds = None

  def accept(self, visitor: ViewVisitor):
    visitor.visitGroup(self)


# A key on the keyboard!
@dataclass
class Key(ViewItem):
  bounds: Rect
  keyboard: 'Keyboard'
  idx: int
  shape: KeyShape
  number: int

  @property
  def black(self):
    return (self.number % 12) in {1, 3, 6, 8, 10}

  def accept(self, visitor: ViewVisitor):
    visitor.visitKey(self)


# A multi-key keyboard. Only one of these in any view.
@dataclass
class Keyboard(Group):
  from_octave: int
  n_octaves: int

  @property
  def from_key(self):
    return 12 * self.from_octave

  @property
  def n_keys(self):
    return 12 * self.n_octaves + 1

  def __post_init__(self):
    return super().__post_init__()

  @property
  def black_keys(self) -> list[Key]:
    return [k for k in self.items if isinstance(k, Key) and k.black]

  @property
  def white_keys(self) -> list[Key]:
    return [k for k in self.items if isinstance(k, Key) and not k.black]

  def accept(self, visitor: ViewVisitor):
    visitor.visitKeyboard(self)


